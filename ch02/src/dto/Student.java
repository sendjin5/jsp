package dto;

public class Student {
    private int no;
    private String name;
    private int kor;
    private int eng;
    private int met;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getKor() {
        return kor;
    }

    public void setKor(int kor) {
        this.kor = kor;
    }

    public int getEng() {
        return eng;
    }

    public void setEng(int eng) {
        this.eng = eng;
    }

    public int getMet() {
        return met;
    }

    public void setMet(int met) {
        this.met = met;
    }
}
